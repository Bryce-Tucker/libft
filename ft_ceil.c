#include "libft.h"

double	ft_ceil(double x)
{
	if ((x > 0 && ((double)((int)x)) + 1 - x > 1) || x == -0)
		return (x);
	return (x <= 0 ? ((double)((int)x)) : ((double)((int)x)) + 1);
}
